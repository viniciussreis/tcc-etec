package br.com.localcode60.modelo;

public class Modelo {

    private int id;
    private String nome;
    private int modeloMarca;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getModeloMarca() {
        return modeloMarca;
    }

    public void setModeloMarca(int modeloMarca) {
        this.modeloMarca = modeloMarca;
    }

}
